    function __init__()
        path_to_lib = @__DIR__
        addHeaderDir(path_to_lib, kind=C_System)
        Libdl.dlopen(path_to_lib * "/libfastcluster.so", Libdl.RTLD_GLOBAL)
        cxxinclude("fastcluster.h")
    end

